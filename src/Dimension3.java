/** 
 * Convenience class for wrapping three 
 * floats to represent dimension in 3D.
 * By: Ira Greenberg 
 * December 2010
 */
 
 public class Dimension3{
  
  public float w, h, d;
  
  public Dimension3(){
  }
  
  public Dimension3(float w, float h, float d){
    this.w = w;
    this.h = h;
    this.d = d;
  }
  
}